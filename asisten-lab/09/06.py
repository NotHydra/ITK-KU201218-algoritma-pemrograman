# => Kita dapat menambahkan parameter pada fungsi
# => Parameter ini kemudian dapat digunakan kembali di dalam fungsi
def helloWorld(bebas, entahlah, apalah):
    print(bebas)
    print(entahlah + apalah)


# => Pada saat kita memanggil fungsi, kita harus memberikan argument
helloWorld("Print", 1, 2)
