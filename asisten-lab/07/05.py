# => Kita dapat mengecek apakah sebuah karakter terdapat pada string
# => Dengan menggunakan operator in dan not in

pulau = "Lampu"

print("m" in pulau)
if "m" in pulau:
    print("m terdapat pada string")

print()  # => Jarak

print("n" not in pulau)
if "n" not in pulau:
    print("n tidak terdapat pada string")
