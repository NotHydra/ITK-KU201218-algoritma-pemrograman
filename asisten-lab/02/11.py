# Kita dapat menggunakan Library (alat bantu) untuk mempermudah kita
# Library dengan nama math digunakan untuk operasi matematika yang lebih kompleks

import math

jariJari = 14

print(math.pi * jariJari**2) 

# Cara Lain

PI = math.pi

print(PI * jariJari**2)



# Implementasi lain dari Library math
print(math.sqrt(16)) # akar kuadrat dari 16
print(math.sin(45)) # sin dari 45 derajat
print(math.cos(45)) # cos dari 45 derajat
print(math.tan(45)) # tan dari 45 derajat
print(math.pow(2, 3)) # 2 pangkat 3
print(math.ceil(3.14)) # pembulatan ke atas
print(math.floor(3.14)) # pembulatan ke bawah
print(math.trunc(3.14)) # pembulatan ke nol

