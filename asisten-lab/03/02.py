# => operasi perbandingan akan selalu menghasilkan value dengan data type boolean (True atau False)
# => jenis-jenis operasi perbandingan:

# => 1. == : "setara dengan" atau "sama dengan"
print(7 == 7)  # => True
print(6 == 3)  # => False

# => 2. != : "tidak setara dengan" atau "tidak sama dengan"
print(7 != 7)  # => False
print(6 != 3)  # => True

# => 3. > : "lebih besar dari"
print(7 > 7)  # => False
print(6 > 3)  # => True

# => 4. < : "lebih kecil dari"
print(7 < 7)  # => False
print(6 < 3)  # => False

# => 5. >= : "lebih besar atau setara dengan" atau "lebih besar atau sama dengan"
print(7 >= 7)  # => True
print(6 >= 3)  # => True

# => 6. <= : "lebih kecil atau setara dengan" atau "lebih kecil atau sama dengan"
print(7 <= 7)  # => True
print(6 <= 3)  # => False
