# => "==" memilik arti "setara dengan" atau "sama dengan"
print(f"Apakah 7 setara dengan 7 = {7 == 7}, {type(7 == 7)}")


# => value dari operasi perbadingan dapat disimpan dalam variabel
valuePerbandingan = 6 == 3  # => juga dapat dibaca sebagai x = (6 == 3)
print(f"Apakah 6 setara dengan 3 = {valuePerbandingan}, {type(valuePerbandingan)}")
