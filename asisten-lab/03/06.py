# => contoh penggunaan if, else if (elif), dan else statement

# => if/elif/else statement yang akan dijalankan hanya satu, sesuai dengan kondisi yang terpenuhi
if 96 >= 86:
    print("Lu dapat A")

elif 96 >= 76:
    print("Lu dapat B")

elif 96 >= 66:
    print("Lu dapat C")

else:  # => else statement tidak wajib ada, ini hanya statement tambahan jika tidak ada kondisi yang terpenuhi
    print("Saya ngulang alpro :D")
