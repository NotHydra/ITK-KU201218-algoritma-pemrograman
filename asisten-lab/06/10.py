# => Fungsi max & min dapat digunakan untuk mencari nilai maksimum & minimum pada List

angkaAngkaAcak = [9, 3, 7, 4, 1, 8, 6, 2, 5, 10]

print(f"List Acak: {angkaAngkaAcak}")
print(f"Maksimum: {max(angkaAngkaAcak)}")
print(f"Minimum: {min(angkaAngkaAcak)}")

print()  # => Jarak

# => Fungsi append dapat digunakan untuk menambahkan Value di akhir List

angkaAngkaAcak.append(17)
print(f"List Acak: {angkaAngkaAcak}")
print(f"Maksimum: {max(angkaAngkaAcak)}")
print(f"Minimum: {min(angkaAngkaAcak)}")
